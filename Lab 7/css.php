			<h2>What is CSS ?</h2>
			<p>	CSS stands for Cascading Style Sheets</p>
			<p>	CSS describes how HTML elements are to be displayed on screen, paper, or in other media</p>
			<p>	CSS saves a lot of work. It can control the layout of multiple web pages all at once</p>
			<p>External stylesheets are stored in CSS files
	</p>
			

			<h2>Why Use CSS?</h2>
			<p>CSS is used to define styles for your web pages, including the design, layout and variations in display for different devices and screen sizes.</p>

			<h2>CSS Solved a Big Problem</h2>
			<p>HTML was NEVER intended to contain tags for formatting a web page!

			HTML was created to describe the content of a web page, like:

This is a heading

			<p>This is a paragraph.</p>

			<p>When tags like, and color attributes were added to the HTML 3.2 specification, it started a nightmare for web developers. Development of large websites, where fonts and color information were added to every single page, became a long and expensive process.

			To solve this problem, the World Wide Web Consortium (W3C) created CSS.

			CSS removed the style formatting from the HTML page!</p>

